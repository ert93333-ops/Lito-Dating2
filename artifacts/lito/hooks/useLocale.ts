type Lang = keyof typeof strings;
type LocaleKey = keyof (typeof strings)["ko"];

export function useLocale() {
  const { profile } = useApp();
  const lang: Lang = profile.language === "ko" ? "ko" : "ja";
  return {
    t: (key: LocaleKey): string => strings[lang][key],
    lang,
  };
}
